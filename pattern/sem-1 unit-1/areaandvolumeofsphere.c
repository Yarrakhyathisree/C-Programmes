#include<stdio.h>
int main()
{
	/*C programme for area and volume of a sphere*/
	int pie=3.14,r,area,volume;
	printf("area of a sphere:");
	scanf("%d%d",&area,&volume);
	area=4*pie*r*r;
	printf("area of sphere=%d",area);
	printf("volume of a sphere:");
	volume=4/3*pie*r*r*r;
	printf("volume of a sphere=%d",volume);
	return 0;
	
}
