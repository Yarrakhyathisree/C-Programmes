#include<stdio.h>
int main()
{
   /* C Programme for area and volume of a cylinder*/
   int area,volume,pie,r,h;
   printf("area of a cylinder:");
   scanf("%d%d",&area,&volume);
   area=2*pie*r(r+h);
   printf("area of a cylinder=%d",area);
   printf("volume of a cylinder:");
   volume=pie*r*r*h;
   printf("volume of cylinder=%d",volume);
   return 0;
   
} 
