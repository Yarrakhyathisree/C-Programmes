#include<stdio.h>
int main()
{
   /*C Programme for area and perimeter of a Isosceles triangle*/
   int area,breadth,height,perimeter,a,b;
   printf("area of a isosceles triangle:");
   scanf("%d",&area);
   area=1/2*breadth*height;
   printf("area of Isosceles triangle is=%d,area");
   printf("perimeter of a Isosceles triangle:");
   perimeter=2*(a+b);
   printf("perimeter of Isosceles triangle is=%d,perimeter");
   return 0;
   
}
