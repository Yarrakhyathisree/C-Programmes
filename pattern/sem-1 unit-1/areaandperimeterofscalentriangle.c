#include<stdio.h>
int main()
{
   /* C Programme for area and perimeter of a Scalene triangle*/
   int area,perimeter,a,b,c,s=(a+b+c)/2;
   printf("area of scalene triangle:");
   scanf("%d",&area);
   area=3.14*s(s-a)*(s-b)*(s-c);
   printf("area of scalene triangle=%d",area);
   printf("perimeter of a Scalene triangle:");
   perimeter=a+b+c;
   printf("perimeter of a Scalene triangle=%d",perimeter);  
   return 0;
   
}
